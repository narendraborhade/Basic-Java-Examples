package com.example.string;

public class StringFunctions {

	public static void main(String[] args) {
		
		String country = "INDIA";
		
		System.out.println("Length is of String is :" + country.length());
		
		System.out.println("Index of character 'D' is of String is :" + country.indexOf("D"));
		
		System.out.println("Index of character 'A' is of String is :" + country.lastIndexOf("A"));

	}

}
